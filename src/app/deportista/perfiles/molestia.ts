export class Molestia {
  titulo:string;
  fecha:string;
  tipo:string;
  descripcion:string;
  constructor(
    titulo:string,
    fecha:string,
    tipo:string,
    descripcion:string
  ){
    this.titulo = titulo;
    this.fecha = fecha;
    this.tipo = tipo;
    this.descripcion = descripcion;
  }
}
